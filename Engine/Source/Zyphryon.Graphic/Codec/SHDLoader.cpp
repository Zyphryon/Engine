    Bool SHDLoader::Load(Ref<Content::Service> Service, Ref<Content::Scope> Scope, AnyRef<Blob> Data)
    {
        const Retainer<Shader> Asset = Retainer<Shader>::Cast(Scope.GetResource());
        Asset->SetSource(Move(Data));
        return true;
    }
