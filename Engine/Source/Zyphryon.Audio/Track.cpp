namespace Audio
{
    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

    Track::Track(AnyRef<Content::Uri> Key)
        : AbstractResource { Move(Key) },
          mFrames          { 0 },
          mFrequency       { 0 },
          mStride          { 0 }
    {
    }

    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

    void Track::Load(UInt64 Frames, UInt32 Frequency, UInt16 Stride, AnyRef<Codec> Codec, AnyRef<Blob> Samples)
    {
        SetFootprint(Samples.GetSize());

        mFrames    = Frames;
        mFrequency = Frequency;
        mStride    = Stride;
        mCodec     = Move(Codec);
        mSamples   = Move(Samples);
    }
}