    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

    Blob Embedded::Read(ConstStr8 Path)
    {
        if (const auto Resource = Zyphryon::RC::Fetch(Path))
        {
            return Blob::Wrap(const_cast<Ptr<Byte>>(Resource->Data), Resource->Size);
        }
        return Blob();
    }

